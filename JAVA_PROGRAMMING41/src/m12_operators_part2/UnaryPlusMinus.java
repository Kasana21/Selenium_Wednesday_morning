package m12_operators_part2;

public class UnaryPlusMinus {
    public static void main(String[] args) {
        int a = 5;

        System.out.println(a);//5

        //+ positive operator is optional
        int b = +a;
        System.out.println(b);//5

        int x = 10;
        int y = -x + 5;
        System.out.println(y);//-5





    }
}
