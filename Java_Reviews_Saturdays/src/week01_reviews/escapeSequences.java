package week01_reviews;

public class escapeSequences {
    public static void main(String[] args) {

        // \t

        System.out.println("\tHello Batch 28 students");

        //\n
        System.out.println("Hello World \nHow are you all today?");

        // \"
        System.out.println("Hello \"CYDEO\" Batch 28 students");

        //\\
        System.out.println("    ^");
        System.out.println("   / \\");
        System.out.println("  /   \\");
        System.out.println("  -----");

    }

}
