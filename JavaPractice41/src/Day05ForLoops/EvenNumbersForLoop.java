package Day05ForLoops;

public class EvenNumbersForLoop {
    public static void main(String[] args) {
        //Print the even numbers between 0 (exclusive) and 10 (inclusive) in the same line with a space between the numbers
        for (int i = 2; i <=10 ; i+=2) {
            System.out.print(i + " ");
        }
    }
}
