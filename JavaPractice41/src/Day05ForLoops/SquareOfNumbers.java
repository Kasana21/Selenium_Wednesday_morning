package Day05ForLoops;

public class SquareOfNumbers {
    public static void main(String[] args) {
        //Print the square of numbers which starts from 1 and ends at 10

        System.out.println("Number\t|\tSquare of Number");
        System.out.println("------------------------");
        for (int i = 1; i < 11; i++) {
            System.out.println("\t"+i+ "\t|\t" + (i*i));
        }



        }

    }

