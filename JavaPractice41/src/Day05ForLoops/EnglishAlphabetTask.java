package Day05ForLoops;

public class EnglishAlphabetTask {
    public static void main(String[] args) {
        /*
        EnglishAlphabets Task: Create a class named EnglishAlphabets, and write a program that displays alphabet letters from A to Z in the same line separated by a space.

         */

        for (char i = 'A'; i <= 'Z'; i++) {
            System.out.print(i + " ");
            }

        }

    }

